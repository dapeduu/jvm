public class ArithmeticOperations {
  public static void main(String[] args) {
    int a = 6;
    int b = 3;

    // Perform addition
    int sum = a + b;

    // Perform subtraction
    int difference = a - b;

    // Perform multiplication
    int product = a * b;

    // Perform increment on a
    int incrementedA = a + 1;

    // Perform decrement on b
    int decrementedB = b - 1;

    // Perform compound assignment (addition)
    int compoundSum = a;
    compoundSum += b;

    // Perform compound assignment (multiplication)
    int compoundProduct = a;
    compoundProduct *= b;

    // Perform bitwise AND
    int bitwiseAnd = a & b;

    // Perform bitwise XOR
    int bitwiseXor = a ^ b;
    
    // Variables sum, difference, product, quotient, remainder, incrementedA, decrementedB,
    // compoundSum, compoundProduct, bitwiseAnd, bitwiseOr, bitwiseXor, leftShift, and rightShift
    // hold the results of the operations
  }
}